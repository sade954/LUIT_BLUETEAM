#Addition with integers
print(2 + 2)
Ouput = 4

#Addition with a float
print(2.0 + 2.0)
Output = 4

#Multiplication with integers
print(3 * 9)
Ouput = 27

#Division with integers
print(5 / 3)
Output = 1.6666666666666667

#Floor Division with integers
print(5 // 3)
Output = 1

#Floor Division with a whole number remainder
print(5 % 3)
Output = 2

#Exponents
print(2 ** 3)
Ouput = 8




